package com.green.second.ch1;

public class VarEx1 {
    public static void main(String[] args) {
        //기본형, primitive type - 8개    소문자로시작, 예약어에 포함
        //정수형
        byte v1;
        short v2;
        int v3;
        long v4;
        //실수형
        double v5;
        float v6;
        //논리형(불린형)
        boolean v7;
        //문자형
        char v8;

        String str; //  참조형, 해당 타입의 주소값을 가짐, 대문자로시작

        v3 = 5;  //대입연산자 =   ,     처음 값을 넣음 - 초기화
        v3 = 10+10; //=은 결과물을 저장하기 때문에 오른쪽 연산부터 실행
    }
}
